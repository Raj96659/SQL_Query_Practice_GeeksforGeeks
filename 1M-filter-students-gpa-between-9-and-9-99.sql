Write an SQL query to print details of the Students whose GPA lies between 9.00 and 9.99.

>> select * from students where GPA between 9.00 and 9.99;

Explanation:

The BETWEEN operator retrieves rows where the GPA falls within the inclusive range of 9.00 to 9.99. 
This query outputs the students who scored high GPAs in the given range, showcasing the top performers.
